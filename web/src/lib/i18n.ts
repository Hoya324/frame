const DICTS: Record<Locale, Dict> = { ko, en, ja };

export function translate(locale: Locale, key: string): string {
  return DICTS[locale][key] ?? ko[key] ?? key;
}

// Render two language forms as "primary (secondary)" when both exist and differ.
// Used to show an original-language title/venue with its other-language name in parens.
export function bilingual(primary?: string | null, secondary?: string | null): string {
  const p = primary?.trim();
  const s = secondary?.trim();
  if (!p) return s ?? "";
  if (!s || s === p) return p;
  return `${p} (${s})`;
}
